/*
  Outros operadores:
  
  () -> representa uma chamada de função;

  [] -> Representa uma chamada para o operador substituível []; exemplo: fooList[1] passa o int 1 para fooList para acessar o elemento no índice 1;

  ?[] -> Como os [], mas o operando mais à esquerda pode ser nulo; exemplo: fooList?[1] passa o int 1 para fooList para acessar o elemento no índice 1, a menos que fooList seja nulo (nesse caso, a expressão é avaliada como nula);

  . -> Refere-se a uma propriedade de uma expressão; exemplo: foo.bar seleciona a barra de propriedades da expressão foo;

  ?. -> Como o ., mas o operando mais à esquerda pode ser nulo; exemplo: foo?.bar seleciona a barra de propriedades da expressão foo, a menos que foo seja nulo (nesse caso, o valor de foo?.bar é nulo).
*/