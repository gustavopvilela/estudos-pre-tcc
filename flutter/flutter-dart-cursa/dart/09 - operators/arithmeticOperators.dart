void main() {
  // operadores aritméticos
  
  print(1 + 2); // operador de soma
  
  print(1 - 2); // operador de subtração
  
  print(1 * 2); // operador de multiplicação
  
  print(4 / 2); // operador de divisão
  
  print(3 % 2); // operador de módulo, ele retorna o resto da divisão, nesse caso, 1
  
  print(3 ~/ 2); // divide, retornando um valor inteiro

  print(-1 - (-2)); // o operador antes dos números inverte o sinal, então é um negativo e dois negativo
}